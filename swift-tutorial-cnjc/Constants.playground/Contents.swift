import Cocoa

/* ------------------------------------ delaration ------------------------------------ */
// let constantName = <initial value>
let constA = 42;
print(constA); // => 42



/* ------------------------------------ type annotation ------------------------------------ */
// var constantName:<data type> = <optional initial value>;
let constB = 42;
print(constB); // => 42

let constC:Float = 3.14159;
print(constC); // => 3.14159



/* ------------------------------------ naming ------------------------------------ */
let _const = "Hello, World!";
print(_const);
let 你好 = "你好，世界!";
print(你好);
let 안녕 = "안녕, 세계!";
print(안녕);



/* ------------------------------------ output ------------------------------------ */
let firstName = "IAN";
let lastName = "CHOI";
print("\(firstName) \(lastName)");
